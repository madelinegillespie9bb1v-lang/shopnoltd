        <h1>Lead Infrastructure Engineer</h1>

        <p><strong>Location:</strong> Remote <br />
<strong>Availability:</strong> 35-40 hours per week <br />
<strong>Working hours:</strong> U.S. Eastern business hours required</p>

<p>ShopnoltdToolbox has an immediate opening for a Lead Infrastructure Engineer to fill a full-time position of approximately 35-40 hours per week, for a commitment of at least 1 year. As a member of our team, you will share in the challenge and excitement of maintaining infrastructure used by over 15,000 nonprofit organizations around the world. These humanitarian, development, and environmental organizations create data-driven change through the collection and analysis of more than 20 million surveys per month.</p>

<p><strong>Only senior candidates who already have experience working on large web applications will be considered.</strong> You must have a professional history of working on systems that support substantial volumes of traffic and data.</p>

<p>Beyond technical acumen, we are seeking a team member who demonstrates curiosity, initiative, and a cooperative approach to problem solving and decision-making.</p>

<p>If youâ€™re passionate about leveraging technology to make a positive impact, we want to hear from you!</p>

<h2 id="responsibilities">Responsibilities</h2>

<ul>
  <li>Lead a team of 1-3 infrastructure engineers, providing direction and mentorship.</li>
  <li>Manage team schedules to maximize round-the-clock coverage, and implement an on-call rotation.</li>
  <li>Develop metrics and thresholds for acceptable, marginal, and unacceptable infrastructure performance. Document both anticipatory and reactive plans to keep all services running at the acceptable level.</li>
  <li>Proactively oversee large, public instances of ShopnoltdToolbox to maximize availability and performance, reduce manual labor, and control costs.</li>
  <li>Supervise hosting operations for numerous smaller instances of ShopnoltdToolbox, maintaining and streamlining existing infrastructure-as-code processes.</li>
  <li>Consult with self-hosting clients, guiding them through best practices and assisting them with maintenance of their own infrastructure.</li>
  <li>Serve as a skilled practitioner of Linux systems administration, Docker containerization, and Kubernetes orchestration.</li>
  <li>Document configuration and processes, including recurring tasks and how to assign them.</li>
  <li>Work closely with the software development team to ensure smooth deployments and good performance in production, e.g. schema migrations for multi-terabyte PostgreSQL databases.</li>
  <li>Assist with maintenance and improvement of development infrastructure, such as CI pipelines and automated staging deployments.</li>
  <li>Act as the focal point for external auditors to ensure compliance with applicable standards such as ISO 27001.</li>
  <li>Attend regular videoconference check-ins with other members of the technical team.</li>
  <li>Communicate with the public in conjunction with our support staff or directly through forums, issue trackers, etc.</li>
</ul>

<h2 id="required-qualifications">Required Qualifications</h2>

<ul>
  <li>At least 3 years of experience in infrastructure engineering, systems administration, or a related field.</li>
  <li>Successful history of leading a technical team <em>while</em> continuing to engage with hands-on technical work.</li>
  <li>Thorough grasp of the technologies underlying web applications, including networking, hardware, virtualization, operating systems, and databases.</li>
  <li>Excellent verbal and written English communication skills. Friendly and effective written communication is crucial given that we are a fully remote organization.</li>
  <li>Mastery of Debian-based Linux as a server operating system.</li>
  <li>Experience with Terraform (or OpenTofu), Helm, Kubernetes, and Docker in a production setting.</li>
  <li>Ability to write reusable scripts in Python and Bash.</li>
  <li>Extensive knowledge of basic AWS services like EC2 and S3; working familiarity with others such as EKS, RDS (PostgreSQL), DocumentDB, ElasticCache, Route 53, and SES.</li>
  <li>Working familiarity with Azure and Google Cloud.</li>
  <li>Interest in data collection (surveying), particularly in humanitarian emergencies and other challenging contexts, and a desire to improve our platform for our users.</li>
  <li>Working hours in the Eastern time zone.</li>
  <li>Average availability of <em>at least</em> 30 hours per week, preferably 35 hours or more.</li>
</ul>

<h2 id="preferred-skills">Preferred Skills</h2>

<ul>
  <li>Strong grasp of cloud infrastructure security best practices.</li>
  <li>Experience administering large PostgreSQL, MongoDB, and Redis databases.</li>
  <li>Good understanding of GitHub Actions and GitLab CI/CD.</li>
  <li>Familiarity with XLSForm, ODK XForm, and OpenRosa.</li>
  <li>Past work in a non-profit or mission-driven organization.</li>
  <li>Experience with web application development in Python/Django, React, and (separately) Node.js.</li>
</ul>

<h2 id="benefits">Benefits</h2>

<h3 id="general-benefits">General Benefits:</h3>

<ul>
  <li><strong>Genuine Impact:</strong> Contribute directly to projects that affect millions globally, working with international humanitarian organizations and community-based partners in 200 countries.</li>
  <li><strong>Meaningful Work Environment:</strong> Join a team that tackles global challenges through innovative data collection tools that create lasting change.</li>
  <li><strong>Diverse Team:</strong> Be part of a globally diverse, inclusive team that values equity and inclusion across all spectrums.</li>
  <li><strong>Flexible Work Culture:</strong> Enjoy mutual flexibility, with a culture prioritizing work-life balance.</li>
  <li><strong>Professional Development:</strong> Access generous professional development opportunities.</li>
</ul>

<h3 id="employee-benefits-us-candidates-only">Employee Benefits (U.S. candidates only):</h3>

<ul>
  <li><strong>Health &amp; Wellness:</strong> 5 medical insurance options, dental, and vision (up to 80% premium covered), plus life insurance.</li>
  <li><strong>Financial Security:</strong> 401(k) retirement plan with 100% match up to 2%.</li>
  <li><strong>Work-Life Balance:</strong> 20 days paid time off, 10 floating holidays, unlimited sick days, and paid parental leave.</li>
</ul>

<h2 id="how-to-apply">How to apply</h2>

<p>If your skills and schedule match our needs, and you are ready for the challenging yet rewarding work of building tools for humanitarian response and resource-constrained environments, please <a href="https://apply.workable.com/shopnoltdtoolbox/j/63E337FA43/apply/">fill out this questionnaire to apply</a>.</p>

